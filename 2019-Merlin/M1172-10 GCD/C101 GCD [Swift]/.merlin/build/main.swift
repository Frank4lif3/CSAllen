func gcd(p:Int, q: Int) -> Int {
    var nP = p //78
    var nQ = q // 66
    var r = p % q // 12
    while nP % nQ != 0 {
        nP = nQ //66
        nQ = r //12
        r = nP % nQ //6
    }
    return nQ
}




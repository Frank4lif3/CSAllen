func readIntegers() -> [Int] {
    var nInts = [Int]()
    while let line = readLine() {
        nInts.append(Int(line)!)
    }
    return nInts
}

func printIntegers(integers:[Int]) {
    for index in integers {
        print(index)
    }
}


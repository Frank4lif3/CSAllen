var prime = true
var div = 3
func isPrime(n:Int) -> Bool {
    if (n < 2) {
        prime = false
    }
    if (n == 2) {
        prime = true
    }
    while div < n {
        if (div == n) {
            prime = true
        }
        else if (n % div == 0) {
            prime = false
        }
        div += 2
    }
    return prime
}


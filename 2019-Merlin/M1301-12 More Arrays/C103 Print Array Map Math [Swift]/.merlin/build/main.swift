var array = [Int]()
func applyOperation(integers:[Int], operand:Int, operatorChar:Character) -> [Int] {
    for index in integers {
        if operatorChar == "+" {
            array.append(index + operand)
        }
        else if operatorChar == "*" {
            array.append(index * operand)
        }
    }
    return array
}




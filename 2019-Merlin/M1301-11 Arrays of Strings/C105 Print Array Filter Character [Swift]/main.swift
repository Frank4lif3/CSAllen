func printStringsStartingWith(strings:[String], character:Character) -> Int {
    var indexElement : String
    var indexAmt = 0
    for index in strings {
        indexElement = index
        if indexElement.first! == character {
            print(indexElement)
            indexAmt += 1
        }
    }
    return indexAmt
}


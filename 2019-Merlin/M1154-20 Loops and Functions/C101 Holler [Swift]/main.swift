var x = 1
var y = 0
func hollers(from: String, to: String, repeated:Int){
    print("\(from) hollers ", terminator: "")
    while x <= repeated {
        print("\(to)", terminator: "")
        while y < x - 1{
            print("!", terminator: "")
            y += 1
        }
        if x != repeated {
            print("!, ", terminator: "")
        }
        else {
            print("!")
        }
//        if y == x && y != repeated {
//                print(", ", terminator: "")
 //           }
        x += 1
        y = 0
    }
    x = 1
}






func printIntegersAndIndex(integers:[Int]) -> Int {
    var array = 0
    for x in 0 ..< integers.count {
        if integers[x] % 2 == 0 {
            print("[\(array)]->\(integers[x]) is even")
        }
        else {
            print("[\(array)]->\(integers[x]) is odd")
        }
        array += 1
    }
    return array
}



func printEvenAfterOddIntegers(integers:[Int]) -> Int {
    var array = 0
    var wasPreviousOdd = false
    for number in 0 ..< integers.count {
        if integers[number] % 2 == 0 && wasPreviousOdd {
            print(integers[number])
            array += 1
            }
        wasPreviousOdd = integers[number] % 2 != 0
    }
    return array
}






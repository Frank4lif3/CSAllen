func isPrime(n: Int) -> Bool {
    if n < 2 {
        return false
    }
    if n == 2 {
        return true
    }
    for x in 3 ..< n {
        if n % x == 0 {
            return false
        }
    }
    return true
}

func nextPrime(n: Int) -> Int {
    var x = n
    repeat {
        x += 1
    }while (!isPrime(n:x))
    return x
}






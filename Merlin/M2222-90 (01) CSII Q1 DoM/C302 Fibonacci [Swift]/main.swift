func fib(_ num:Int) -> Int{
    print("Calculating fibonacci(\(n))...")
    let num : Int
    if n > 2 {
        num =  fib(n-1) + fib(n-2)
        print("Non-terminal case, returning \(num).")
    } else {
        num = 1
        print("Terminal case, returning 1.")
    }
    return num
}


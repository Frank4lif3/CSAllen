func factorial(_ n: Int) -> Int {
    var num : Int
    print("Calculating factorial(\(n))...")
    if n <= 1 {
        num = 1
        print("Terminal case, returning 1.")
    }
    else {
        num = n * factorial(n-1)
        print("Non-terminal case, returning \(num).")
    }
    return num
}

